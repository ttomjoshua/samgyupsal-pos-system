do $$
declare
  legacy_products_exists boolean;
  legacy_sale_items_exists boolean;
begin
  select exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'products'
      and column_name = 'category'
  )
  and exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'products'
      and column_name = 'product_name'
  )
  and not exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'products'
      and column_name = 'category_id'
  )
  into legacy_products_exists;

  if legacy_products_exists then
    if to_regclass('public.products_legacy') is null then
      alter table public.products rename to products_legacy;
    else
      raise exception 'Legacy migration halted: public.products still has the old schema, but public.products_legacy already exists. Review the tables manually before rerunning this script.';
    end if;
  end if;

  select exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'sale_items'
      and column_name = 'product_id'
  )
  and not exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'sale_items'
      and column_name = 'inventory_item_id'
  )
  into legacy_sale_items_exists;

  if legacy_sale_items_exists then
    if to_regclass('public.sale_items_legacy') is null then
      alter table public.sale_items rename to sale_items_legacy;
    else
      raise exception 'Legacy migration halted: public.sale_items still has the old schema, but public.sale_items_legacy already exists. Review the tables manually before rerunning this script.';
    end if;
  end if;
end
$$;

