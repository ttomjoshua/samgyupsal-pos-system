do $$
begin
  if to_regclass('public.products_legacy') is not null
     and not exists (select 1 from public.products limit 1)
     and not exists (select 1 from public.inventory_items limit 1) then
    insert into public.categories (name, slug, created_at, updated_at)
    select distinct
      category_clean,
      category_slug,
      now(),
      now()
    from (
      select
        case
          when btrim(coalesce(category, '')) = '' then 'Uncategorized'
          else regexp_replace(btrim(category), '\s+', ' ', 'g')
        end as category_clean,
        regexp_replace(
          lower(
            regexp_replace(
              case
                when btrim(coalesce(category, '')) = '' then 'Uncategorized'
                else regexp_replace(btrim(category), '\s+', ' ', 'g')
              end,
              '[^a-zA-Z0-9]+',
              '-',
              'g'
            )
          ),
          '(^-+|-+$)',
          '',
          'g'
        ) as category_slug
      from public.products_legacy
    ) category_seed
    on conflict (slug) do update
    set
      name = excluded.name,
      updated_at = now();

    insert into public.products (
      category_id,
      product_name,
      unit_label,
      default_price,
      legacy_price_text,
      is_active,
      created_at,
      updated_at
    )
    with legacy_catalog as (
      select
        category_clean,
        product_name_clean,
        unit_label_clean,
        max(price_numeric) as default_price,
        max(nullif(price_text, '')) as legacy_price_text,
        min(coalesce(created_at, now())) as created_at
      from (
        select
          case
            when btrim(coalesce(category, '')) = '' then 'Uncategorized'
            else regexp_replace(btrim(category), '\s+', ' ', 'g')
          end as category_clean,
          regexp_replace(btrim(coalesce(product_name, 'Unnamed Product')), '\s+', ' ', 'g') as product_name_clean,
          coalesce(regexp_replace(btrim(coalesce(net_weight, '')), '\s+', ' ', 'g'), '') as unit_label_clean,
          btrim(coalesce(price, '')) as price_text,
          case
            when btrim(coalesce(price, '')) ~ '^\d+(\.\d+)?$' then btrim(price)::numeric(12, 2)
            else null
          end as price_numeric,
          created_at
        from public.products_legacy
      ) normalized_rows
      group by 1, 2, 3
    )
    select
      c.id,
      legacy_catalog.product_name_clean,
      legacy_catalog.unit_label_clean,
      legacy_catalog.default_price,
      legacy_catalog.legacy_price_text,
      true,
      legacy_catalog.created_at,
      now()
    from legacy_catalog
    join public.categories c
      on c.name = legacy_catalog.category_clean;

    insert into public.inventory_items (
      branch_id,
      product_id,
      selling_price,
      stock_quantity,
      reorder_level,
      expiration_date,
      legacy_stock_text,
      is_active,
      created_at,
      updated_at
    )
    with legacy_inventory as (
      select
        coalesce(branch_id, 1) as branch_id,
        category_clean,
        product_name_clean,
        unit_label_clean,
        max(price_numeric) as selling_price,
        sum(stock_quantity_numeric) as stock_quantity,
        max(expiration_date_parsed) as expiration_date,
        max(nullif(stock_text, '')) as legacy_stock_text,
        min(coalesce(created_at, now())) as created_at
      from (
        select
          coalesce(branch_id, 1) as branch_id,
          case
            when btrim(coalesce(category, '')) = '' then 'Uncategorized'
            else regexp_replace(btrim(category), '\s+', ' ', 'g')
          end as category_clean,
          regexp_replace(btrim(coalesce(product_name, 'Unnamed Product')), '\s+', ' ', 'g') as product_name_clean,
          coalesce(regexp_replace(btrim(coalesce(net_weight, '')), '\s+', ' ', 'g'), '') as unit_label_clean,
          case
            when btrim(coalesce(price, '')) ~ '^\d+(\.\d+)?$' then btrim(price)::numeric(12, 2)
            else null
          end as price_numeric,
          btrim(coalesce(stock_quantity, '')) as stock_text,
          case
            when regexp_replace(btrim(coalesce(stock_quantity, '')), '[^0-9-]', '', 'g') ~ '^-?\d+$'
              then greatest(regexp_replace(btrim(stock_quantity), '[^0-9-]', '', 'g')::integer, 0)
            else 0
          end as stock_quantity_numeric,
          case
            when btrim(coalesce(expiration_date, '')) ~ '^\d{4}-\d{2}-\d{2}$'
              then btrim(expiration_date)::date
            else null
          end as expiration_date_parsed,
          created_at
        from public.products_legacy
      ) normalized_rows
      group by 1, 2, 3, 4
    )
    select
      legacy_inventory.branch_id,
      p.id,
      coalesce(legacy_inventory.selling_price, p.default_price, 0),
      legacy_inventory.stock_quantity,
      10,
      legacy_inventory.expiration_date,
      legacy_inventory.legacy_stock_text,
      true,
      legacy_inventory.created_at,
      now()
    from legacy_inventory
    join public.categories c
      on c.name = legacy_inventory.category_clean
    join public.products p
      on p.category_id = c.id
     and p.product_name = legacy_inventory.product_name_clean
     and p.unit_label = legacy_inventory.unit_label_clean;
  end if;
end
$$;

create or replace view public.product_catalog_view as
select
  p.id as product_id,
  p.category_id,
  c.name as category_name,
  p.product_name,
  p.unit_label,
  p.default_price,
  p.legacy_price_text,
  p.is_active,
  p.created_at,
  p.updated_at
from public.products p
join public.categories c
  on c.id = p.category_id;

create or replace view public.inventory_catalog_view as
select
  i.id as inventory_item_id,
  i.branch_id,
  b.code as branch_code,
  b.name as branch_name,
  p.id as product_id,
  p.category_id,
  c.name as category_name,
  p.product_name,
  p.unit_label,
  coalesce(i.selling_price, p.default_price, 0) as price,
  p.default_price,
  i.selling_price,
  i.stock_quantity,
  i.reorder_level,
  i.expiration_date,
  i.legacy_stock_text,
  i.is_active,
  i.created_at,
  i.updated_at
from public.inventory_items i
join public.products p
  on p.id = i.product_id
join public.categories c
  on c.id = p.category_id
join public.branches b
  on b.id = i.branch_id;

commit;
