-- FEFO batch inventory, movement ledger, and checkout allocation RPC.
--
-- Run after:
--   - 19_transactional_checkout_rpc.sql
--
-- Purpose:
--   Add physical inventory batches, movement history, sale-to-batch allocation
--   records, stock-in/adjustment RPCs, and replace checkout stock deduction with
--   earliest-expiring-batch-first allocation.

begin;

do $$
begin
  if to_regprocedure('private.current_user_is_admin()') is null
     or to_regprocedure('private.current_user_is_active()') is null
     or to_regprocedure('private.current_user_branch_id()') is null
     or to_regclass('public.profiles') is null then
    raise exception
      'Run the auth rollout and role-policy scripts before 20_fefo_batch_inventory.sql.';
  end if;

  if to_regclass('public.products') is null
     or to_regclass('public.branches') is null
     or to_regclass('public.sales') is null
     or to_regclass('public.sale_items') is null then
    raise exception
      'Run the products, branches, sales, and sale_items schema before 20_fefo_batch_inventory.sql.';
  end if;
end
$$;

create table if not exists public.inventory_batches (
  id bigint generated by default as identity primary key,
  product_id bigint not null references public.products (id) on update cascade on delete cascade,
  branch_id integer not null references public.branches (id) on update cascade on delete restrict,
  batch_code text,
  quantity_received integer not null default 0 check (quantity_received >= 0),
  quantity_on_hand integer not null default 0 check (quantity_on_hand >= 0),
  expiration_date date,
  stock_in_date date not null default current_date,
  source text not null default 'stock-in'
    check (source in ('stock-in', 'adjustment', 'opening-balance')),
  notes text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.inventory_movements (
  id bigint generated by default as identity primary key,
  product_id bigint not null references public.products (id) on update cascade on delete cascade,
  batch_id bigint references public.inventory_batches (id) on update cascade on delete set null,
  branch_id integer not null references public.branches (id) on update cascade on delete restrict,
  movement_type text not null
    check (movement_type in ('stock-in', 'sale', 'adjustment', 'correction', 'opening-balance')),
  quantity_delta integer not null,
  quantity_after integer not null default 0 check (quantity_after >= 0),
  sale_id bigint references public.sales (id) on update cascade on delete set null,
  sale_item_id bigint references public.sale_items (id) on update cascade on delete set null,
  reference text,
  notes text,
  created_by uuid references auth.users (id) on delete set null,
  created_at timestamptz not null default now()
);

create table if not exists public.sale_item_batch_allocations (
  id bigint generated by default as identity primary key,
  sale_id bigint not null references public.sales (id) on update cascade on delete cascade,
  sale_item_id bigint not null references public.sale_items (id) on update cascade on delete cascade,
  product_id bigint not null references public.products (id) on update cascade on delete cascade,
  batch_id bigint not null references public.inventory_batches (id) on update cascade on delete restrict,
  branch_id integer not null references public.branches (id) on update cascade on delete restrict,
  quantity integer not null check (quantity > 0),
  expiration_date date,
  created_at timestamptz not null default now()
);

create index if not exists idx_inventory_batches_product_branch
  on public.inventory_batches (product_id, branch_id);

create index if not exists idx_inventory_batches_fefo_available
  on public.inventory_batches (branch_id, product_id, expiration_date, stock_in_date, id)
