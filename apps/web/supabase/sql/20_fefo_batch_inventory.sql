create or replace function private.adjust_inventory_stock_count(
  p_product_id bigint,
  p_branch_id integer,
  p_target_quantity integer,
  p_expiration_date date default null,
  p_notes text default null
)
returns jsonb
language plpgsql
security definer
set search_path = public, private
as $$
declare
  product_row public.products%rowtype;
  product_branch_id integer;
  current_quantity integer := 0;
  quantity_difference integer := 0;
  remaining_quantity integer := 0;
  adjusted_quantity integer := 0;
  batch_quantity_after integer := 0;
  batch_row public.inventory_batches%rowtype;
  created_batch public.inventory_batches%rowtype;
begin
  perform private.ensure_inventory_batch_user_scope(p_branch_id);

  if p_product_id is null or p_branch_id is null then
    raise exception 'Product and branch are required for stock adjustment.'
      using errcode = '22023';
  end if;

  if coalesce(p_target_quantity, -1) < 0 then
    raise exception 'Adjusted stock quantity cannot be negative.'
      using errcode = '22023';
  end if;

  select *
  into product_row
  from public.products
  where id = p_product_id
  for update;

  if not found then
    raise exception 'The selected product is no longer available.'
      using errcode = '23503';
  end if;

  product_branch_id := private.resolve_product_branch_id(
    product_row.branch_id,
    product_row.branch
  );

  if product_branch_id is distinct from p_branch_id then
    raise exception 'The selected product belongs to a different branch.'
      using errcode = '42501';
  end if;

  select coalesce(sum(quantity_on_hand), 0)::integer
  into current_quantity
  from public.inventory_batches
  where product_id = p_product_id
    and branch_id = p_branch_id;

  quantity_difference := p_target_quantity - current_quantity;

  if quantity_difference = 0 then
    return jsonb_build_object(
      'ok', true,
      'stock_quantity', current_quantity
    );
  end if;

  if quantity_difference > 0 then
    if p_expiration_date is null then
      raise exception 'Increasing adjusted stock requires an expiration date for the added batch.'
        using errcode = '22023';
    end if;

    insert into public.inventory_batches (
      product_id,
      branch_id,
      batch_code,
      quantity_received,
      quantity_on_hand,
      expiration_date,
      stock_in_date,
      source,
      notes
    )
    values (
      p_product_id,
      p_branch_id,
      'ADJ-' || p_product_id::text || '-' || to_char(now(), 'YYYYMMDDHH24MISSMS'),
      quantity_difference,
      quantity_difference,
      p_expiration_date,
      current_date,
      'adjustment',
      nullif(btrim(coalesce(p_notes, '')), '')
    )
    returning *
    into created_batch;

    insert into public.inventory_movements (
      product_id,
      batch_id,
      branch_id,
      movement_type,
      quantity_delta,
      quantity_after,
      reference,
      notes,
      created_by
    )
    values (
      p_product_id,
      created_batch.id,
      p_branch_id,
      'adjustment',
      quantity_difference,
      quantity_difference,
      created_batch.batch_code,
      created_batch.notes,
      auth.uid()
    );
  else
    remaining_quantity := abs(quantity_difference);

    for batch_row in
      select *
      from public.inventory_batches
      where product_id = p_product_id
        and branch_id = p_branch_id
        and quantity_on_hand > 0
      order by expiration_date desc nulls first, stock_in_date desc, id desc
      for update
    loop
      exit when remaining_quantity <= 0;

      adjusted_quantity := least(remaining_quantity, batch_row.quantity_on_hand);

      update public.inventory_batches
      set quantity_on_hand = quantity_on_hand - adjusted_quantity
      where id = batch_row.id
      returning quantity_on_hand
      into batch_quantity_after;

      insert into public.inventory_movements (
        product_id,
        batch_id,
        branch_id,
        movement_type,
        quantity_delta,
        quantity_after,
        reference,
        notes,
        created_by
      )
      values (
        p_product_id,
        batch_row.id,
        p_branch_id,
        'adjustment',
        -adjusted_quantity,
        batch_quantity_after,
        batch_row.batch_code,
        nullif(btrim(coalesce(p_notes, '')), ''),
        auth.uid()
      );

      remaining_quantity := remaining_quantity - adjusted_quantity;
    end loop;

    if remaining_quantity > 0 then
      raise exception 'Adjusted stock is lower than available batch stock but could not be allocated.'
        using errcode = '23514';
    end if;
  end if;

  return jsonb_build_object(
    'ok', true,
    'stock_quantity', private.recalculate_product_stock(p_product_id)
  );
end;
$$;

