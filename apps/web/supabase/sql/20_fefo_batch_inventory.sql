create or replace function private.create_checkout_sale(
  p_sale jsonb,
  p_items jsonb
)
returns jsonb
language plpgsql
security definer
set search_path = public, private
as $$
declare
  current_user_id uuid := auth.uid();
  current_role_key text;
  current_branch_id integer;
  current_status text;
  sale_branch_id integer;
  sale_branch_name text;
  created_sale public.sales%rowtype;
  created_items jsonb := '[]'::jsonb;
  created_allocations jsonb := '[]'::jsonb;
  invalid_item_count integer := 0;
  mismatched_line_total_count integer := 0;
  expected_product_count integer := 0;
  locked_product_count integer := 0;
  product_row record;
  product_branch_id integer;
  product_available_quantity integer := 0;
  sale_payment_method text;
  sale_subtotal numeric := 0;
  sale_discount numeric := 0;
  sale_total_amount numeric := 0;
  sale_cash_received numeric := 0;
  sale_change_amount numeric := 0;
  calculated_product_subtotal numeric := 0;
  calculated_line_total numeric := 0;
  calculated_total_amount numeric := 0;
  sale_item_row public.sale_items%rowtype;
  batch_row public.inventory_batches%rowtype;
  allocation_row public.sale_item_batch_allocations%rowtype;
  remaining_quantity integer := 0;
  allocated_quantity integer := 0;
  batch_quantity_after integer := 0;
begin
  if coalesce(auth.role(), '') <> 'authenticated' or current_user_id is null then
    raise exception 'Only authenticated accounts can complete checkout.'
      using errcode = '42501';
  end if;

  select
    role_key,
    branch_id,
    status
  into
    current_role_key,
    current_branch_id,
    current_status
  from public.profiles
  where id = current_user_id
  limit 1;

  if current_status is distinct from 'active' then
    raise exception 'Only active accounts can complete checkout.'
      using errcode = '42501';
  end if;

  if jsonb_typeof(p_sale) is distinct from 'object' then
    raise exception 'Checkout sale payload must be a JSON object.'
      using errcode = '22023';
  end if;

  if jsonb_typeof(p_items) is distinct from 'array' then
    raise exception 'Checkout items payload must be a JSON array.'
      using errcode = '22023';
  end if;

  if jsonb_array_length(p_items) = 0 then
    raise exception 'Checkout requires at least one line item.'
      using errcode = '22023';
  end if;

  if nullif(p_sale->>'cashier_id', '') is distinct from current_user_id::text then
    raise exception 'Checkout cashier must match the authenticated account.'
      using errcode = '42501';
  end if;

  sale_branch_id := nullif(p_sale->>'branch_id', '')::integer;

  if sale_branch_id is null then
    raise exception 'Checkout branch is required.'
      using errcode = '22023';
  end if;

  select name
  into sale_branch_name
  from public.branches
  where id = sale_branch_id
  limit 1;

  if sale_branch_name is null then
    raise exception 'Checkout branch is not available.'
      using errcode = '23503';
  end if;

  if coalesce(current_role_key, '') <> 'admin'
     and sale_branch_id is distinct from current_branch_id then
    raise exception 'Employee accounts can only complete checkout for their assigned branch.'
      using errcode = '42501';
  end if;

  sale_payment_method := lower(coalesce(nullif(p_sale->>'payment_method', ''), 'cash'));
  sale_subtotal := coalesce(nullif(p_sale->>'subtotal', '')::numeric, 0);
  sale_discount := coalesce(nullif(p_sale->>'discount', '')::numeric, 0);
  sale_total_amount := coalesce(nullif(p_sale->>'total_amount', '')::numeric, 0);
  sale_cash_received := coalesce(nullif(p_sale->>'cash_received', '')::numeric, 0);
  sale_change_amount := coalesce(nullif(p_sale->>'change_amount', '')::numeric, 0);

  if sale_payment_method <> 'cash' then
    raise exception 'Only cash checkout is currently supported.'
      using errcode = '22023';
  end if;

  if sale_subtotal < 0
     or sale_discount < 0
     or sale_total_amount < 0
     or sale_cash_received < 0
     or sale_change_amount < 0 then
    raise exception 'Checkout amounts cannot be negative.'
      using errcode = '22023';
  end if;

  with parsed_items as (
    select
      nullif(item->>'product_id', '')::bigint as product_id,
      nullif(item->>'item_name', '') as item_name,
      coalesce(nullif(item->>'quantity', '')::integer, 0) as quantity,
      coalesce(nullif(item->>'unit_price', '')::numeric, 0) as unit_price,
      coalesce(nullif(item->>'line_total', '')::numeric, 0) as line_total
    from jsonb_array_elements(p_items) as source(item)
  )
  select count(*)
  into invalid_item_count
  from parsed_items
  where item_name is null
     or quantity <= 0
     or unit_price < 0
     or line_total < 0;

  if invalid_item_count > 0 then
    raise exception 'Checkout contains invalid line items.'
      using errcode = '22023';
  end if;

  with parsed_items as (
    select
      nullif(item->>'product_id', '')::bigint as product_id,
      coalesce(nullif(item->>'quantity', '')::integer, 0) as quantity,
      coalesce(nullif(item->>'unit_price', '')::numeric, 0) as unit_price,
      coalesce(nullif(item->>'line_total', '')::numeric, 0) as line_total
    from jsonb_array_elements(p_items) as source(item)
  )
  select
    coalesce(sum(case when product_id is not null then line_total else 0 end), 0),
    coalesce(sum(line_total), 0),
    count(*) filter (
      where round(line_total, 2) <> round(quantity * unit_price, 2)
    )
  into
    calculated_product_subtotal,
    calculated_line_total,
    mismatched_line_total_count
  from parsed_items;

  calculated_total_amount := greatest(0, calculated_line_total - sale_discount);

  if mismatched_line_total_count > 0 then
    raise exception 'Checkout line totals must match quantity and unit price.'
      using errcode = '22023';
  end if;

  if round(sale_subtotal, 2) <> round(calculated_product_subtotal, 2) then
    raise exception 'Checkout subtotal does not match product line items.'
      using errcode = '22023';
  end if;

  if sale_discount > calculated_product_subtotal then
    raise exception 'Checkout discount cannot be greater than the product subtotal.'
      using errcode = '22023';
  end if;

  if round(sale_total_amount, 2) <> round(calculated_total_amount, 2) then
    raise exception 'Checkout total does not match line items and discount.'
      using errcode = '22023';
  end if;

  if sale_cash_received < sale_total_amount then
    raise exception 'Cash received must be equal to or greater than total amount.'
      using errcode = '22023';
  end if;

  if round(sale_change_amount, 2) <> round(sale_cash_received - sale_total_amount, 2) then
    raise exception 'Checkout change does not match cash received and total amount.'
      using errcode = '22023';
  end if;

  with parsed_items as (
    select
      nullif(item->>'product_id', '')::bigint as product_id,
      coalesce(nullif(item->>'quantity', '')::integer, 0) as quantity
    from jsonb_array_elements(p_items) as source(item)
  ),
  product_quantities as (
    select
      product_id,
      sum(quantity)::integer as total_quantity
    from parsed_items
    where product_id is not null
    group by product_id
  )
  select count(*)
  into expected_product_count
  from product_quantities;

  for product_row in
    with parsed_items as (
      select
        nullif(item->>'product_id', '')::bigint as product_id,
        coalesce(nullif(item->>'quantity', '')::integer, 0) as quantity
      from jsonb_array_elements(p_items) as source(item)
    ),
    product_quantities as (
      select
        product_id,
        sum(quantity)::integer as total_quantity
      from parsed_items
      where product_id is not null
      group by product_id
    )
    select
      product.id,
      product.branch_id,
      product.branch,
      product.product_name,
      product.stock_quantity,
      product.is_active,
      product_quantities.total_quantity
    from product_quantities
    join public.products product
      on product.id = product_quantities.product_id
    order by product.id
    for update of product
  loop
    locked_product_count := locked_product_count + 1;
    product_branch_id := private.resolve_product_branch_id(
      product_row.branch_id,
      product_row.branch
    );

    if coalesce(product_row.is_active, true) is not true then
      raise exception '% is not available for checkout.', product_row.product_name
        using errcode = '23514';
    end if;

    if product_branch_id is null
       or product_branch_id is distinct from sale_branch_id then
      raise exception '% belongs to a different branch.', product_row.product_name
        using errcode = '42501';
    end if;

    select coalesce(sum(quantity_on_hand), 0)::integer
    into product_available_quantity
    from public.inventory_batches
    where product_id = product_row.id
      and branch_id = sale_branch_id
      and quantity_on_hand > 0;

    if product_row.total_quantity > product_available_quantity then
      raise exception '% only has % item(s) available in FEFO batches.',
        product_row.product_name,
        product_available_quantity
        using errcode = '23514';
    end if;
  end loop;

  if locked_product_count <> expected_product_count then
    raise exception 'One or more checkout items are no longer available.'
      using errcode = '23503';
  end if;

  insert into public.sales (
    cashier_id,
    cashier_name,
    branch_id,
    branch_name,
    payment_method,
    subtotal,
    discount,
    total_amount,
    cash_received,
    change_amount,
    submitted_at,
    notes
  )
  values (
    current_user_id::text,
    nullif(p_sale->>'cashier_name', ''),
    sale_branch_id,
    sale_branch_name,
    sale_payment_method,
    sale_subtotal,
    sale_discount,
    sale_total_amount,
    sale_cash_received,
    sale_change_amount,
    coalesce(nullif(p_sale->>'submitted_at', '')::timestamptz, now()),
    nullif(p_sale->>'notes', '')
  )
  returning *
  into created_sale;

  with parsed_items as (
    select
      ordinality,
      nullif(item->>'product_id', '')::bigint as product_id,
      nullif(item->>'inventory_item_id', '')::bigint as inventory_item_id,
      nullif(item->>'item_name', '') as item_name,
      coalesce(nullif(item->>'quantity', '')::integer, 0) as quantity,
      coalesce(nullif(item->>'unit_price', '')::numeric, 0) as unit_price,
      coalesce(nullif(item->>'line_total', '')::numeric, 0) as line_total
    from jsonb_array_elements(p_items) with ordinality as source(item, ordinality)
  )
  insert into public.sale_items (
    sale_id,
    product_id,
    inventory_item_id,
    item_name,
    quantity,
    unit_price,
    line_total
  )
  select
    created_sale.id,
    product_id,
    coalesce(inventory_item_id, product_id),
    item_name,
    quantity,
    unit_price,
    line_total
  from parsed_items
  order by ordinality;

  for sale_item_row in
    select *
    from public.sale_items
    where sale_id = created_sale.id
      and product_id is not null
    order by id
  loop
    remaining_quantity := sale_item_row.quantity;

    for batch_row in
      select *
      from public.inventory_batches
      where product_id = sale_item_row.product_id
        and branch_id = sale_branch_id
        and quantity_on_hand > 0
      order by expiration_date asc nulls last, stock_in_date asc, id asc
      for update
    loop
      exit when remaining_quantity <= 0;

      allocated_quantity := least(remaining_quantity, batch_row.quantity_on_hand);

      update public.inventory_batches
      set quantity_on_hand = quantity_on_hand - allocated_quantity
      where id = batch_row.id
      returning quantity_on_hand
      into batch_quantity_after;

      insert into public.sale_item_batch_allocations (
        sale_id,
        sale_item_id,
        product_id,
        batch_id,
        branch_id,
        quantity,
        expiration_date
      )
      values (
        created_sale.id,
        sale_item_row.id,
        sale_item_row.product_id,
        batch_row.id,
        sale_branch_id,
        allocated_quantity,
        batch_row.expiration_date
      )
      returning *
      into allocation_row;

      created_allocations := created_allocations || jsonb_build_array(to_jsonb(allocation_row));

      insert into public.inventory_movements (
        product_id,
        batch_id,
        branch_id,
        movement_type,
        quantity_delta,
        quantity_after,
        sale_id,
        sale_item_id,
        reference,
        created_by
      )
      values (
        sale_item_row.product_id,
        batch_row.id,
        sale_branch_id,
        'sale',
        -allocated_quantity,
        batch_quantity_after,
        created_sale.id,
        sale_item_row.id,
        created_sale.id::text,
        current_user_id
      );

      remaining_quantity := remaining_quantity - allocated_quantity;
    end loop;

    if remaining_quantity > 0 then
      raise exception '% could not be fully allocated to FEFO batches.',
        sale_item_row.item_name
        using errcode = '23514';
    end if;

    perform private.recalculate_product_stock(sale_item_row.product_id);
  end loop;

  select coalesce(jsonb_agg(to_jsonb(saved_item) order by saved_item.id), '[]'::jsonb)
  into created_items
  from public.sale_items saved_item
  where saved_item.sale_id = created_sale.id;

  return jsonb_build_object(
    'ok', true,
    'sale', to_jsonb(created_sale),
    'items', created_items,
    'allocations', created_allocations,
    'inventory_synced', true
  );
end;
$$;

