create or replace function private.ensure_inventory_batch_user_scope(
  p_branch_id integer
)
returns void
language plpgsql
security definer
set search_path = public, private
as $$
declare
  current_user_id uuid := auth.uid();
  current_role_key text;
  current_branch_id integer;
  current_status text;
begin
  if coalesce(auth.role(), '') <> 'authenticated' or current_user_id is null then
    raise exception 'Only authenticated accounts can update inventory batches.'
      using errcode = '42501';
  end if;

  select role_key, branch_id, status
  into current_role_key, current_branch_id, current_status
  from public.profiles
  where id = current_user_id
  limit 1;

  if current_status is distinct from 'active' then
    raise exception 'Only active accounts can update inventory batches.'
      using errcode = '42501';
  end if;

  if coalesce(current_role_key, '') <> 'admin'
     and p_branch_id is distinct from current_branch_id then
    raise exception 'Employee accounts can only update inventory for their assigned branch.'
      using errcode = '42501';
  end if;
end;
$$;

revoke all on function private.ensure_inventory_batch_user_scope(integer) from public;
grant execute on function private.ensure_inventory_batch_user_scope(integer) to authenticated;

create or replace function private.stock_in_inventory_batch(
  p_product_id bigint,
  p_branch_id integer,
  p_quantity integer,
  p_expiration_date date,
  p_stock_in_date date default current_date,
  p_notes text default null
)
returns jsonb
language plpgsql
security definer
set search_path = public, private
as $$
declare
  product_row public.products%rowtype;
  product_branch_id integer;
  created_batch public.inventory_batches%rowtype;
begin
  perform private.ensure_inventory_batch_user_scope(p_branch_id);

  if p_product_id is null or p_branch_id is null then
    raise exception 'Product and branch are required for stock-in.'
      using errcode = '22023';
  end if;

  if coalesce(p_quantity, 0) <= 0 then
    raise exception 'Stock-in quantity must be greater than zero.'
      using errcode = '22023';
  end if;

  if p_expiration_date is null then
    raise exception 'Stock-in requires an expiration date for FEFO tracking.'
      using errcode = '22023';
  end if;

  select *
  into product_row
  from public.products
  where id = p_product_id
  for update;

  if not found then
    raise exception 'The selected product is no longer available.'
      using errcode = '23503';
  end if;

  product_branch_id := private.resolve_product_branch_id(
    product_row.branch_id,
    product_row.branch
  );

  if product_branch_id is distinct from p_branch_id then
    raise exception 'The selected product belongs to a different branch.'
      using errcode = '42501';
  end if;

  if coalesce(product_row.is_active, true) is not true then
    raise exception 'Inactive products cannot receive stock.'
      using errcode = '23514';
  end if;

  insert into public.inventory_batches (
    product_id,
    branch_id,
    batch_code,
    quantity_received,
    quantity_on_hand,
    expiration_date,
    stock_in_date,
    source,
    notes
  )
  values (
    p_product_id,
    p_branch_id,
    'BATCH-' || p_product_id::text || '-' || to_char(now(), 'YYYYMMDDHH24MISSMS'),
    p_quantity,
    p_quantity,
    p_expiration_date,
    coalesce(p_stock_in_date, current_date),
    'stock-in',
    nullif(btrim(coalesce(p_notes, '')), '')
  )
  returning *
  into created_batch;

  insert into public.inventory_movements (
    product_id,
    batch_id,
    branch_id,
    movement_type,
    quantity_delta,
    quantity_after,
    reference,
    notes,
    created_by
  )
  values (
    p_product_id,
    created_batch.id,
    p_branch_id,
    'stock-in',
    p_quantity,
    p_quantity,
    created_batch.batch_code,
    created_batch.notes,
    auth.uid()
  );

  perform private.recalculate_product_stock(p_product_id);

  return jsonb_build_object(
    'ok', true,
    'batch', to_jsonb(created_batch)
  );
end;
$$;

