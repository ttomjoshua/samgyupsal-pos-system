create or replace function private.resolve_product_branch_id(
  p_branch_id integer,
  p_branch_name text
)
returns integer
language sql
stable
set search_path = public, private
as $$
  select coalesce(
    p_branch_id,
    (
      select b.id
      from public.branches b
      where b.name = p_branch_name
      limit 1
    ),
    case
      when p_branch_name = 'Dollar' then 2
      when p_branch_name = 'Sta. Lucia' then 1
      else null
    end
  );
$$;

create or replace function private.recalculate_product_stock(
  p_product_id bigint
)
returns integer
language plpgsql
security definer
set search_path = public, private
as $$
declare
  next_stock_quantity integer := 0;
begin
  select coalesce(sum(quantity_on_hand), 0)::integer
  into next_stock_quantity
  from public.inventory_batches
  where product_id = p_product_id;

  update public.products
  set stock_quantity = next_stock_quantity
  where id = p_product_id;

  return next_stock_quantity;
end;
$$;

revoke all on function private.resolve_product_branch_id(integer, text) from public;
revoke all on function private.recalculate_product_stock(bigint) from public;
grant execute on function private.resolve_product_branch_id(integer, text) to authenticated;
grant execute on function private.recalculate_product_stock(bigint) to authenticated;

insert into public.inventory_batches (
  product_id,
  branch_id,
  batch_code,
  quantity_received,
  quantity_on_hand,
  expiration_date,
  stock_in_date,
  source,
  notes
)
select
  product_row.id,
  branch_scope.branch_id,
  'OPENING-' || product_row.id::text,
  greatest(coalesce(product_row.stock_quantity, 0), 0),
  greatest(coalesce(product_row.stock_quantity, 0), 0),
  product_row.expiration_date,
  current_date,
  'opening-balance',
  'Migrated from products.stock_quantity during FEFO batch rollout.'
from public.products product_row
cross join lateral (
  select private.resolve_product_branch_id(product_row.branch_id, product_row.branch) as branch_id
) branch_scope
where coalesce(product_row.stock_quantity, 0) > 0
  and branch_scope.branch_id is not null
  and not exists (
    select 1
    from public.inventory_batches existing_batch
    where existing_batch.product_id = product_row.id
  );

insert into public.inventory_movements (
  product_id,
  batch_id,
  branch_id,
  movement_type,
  quantity_delta,
  quantity_after,
  reference,
  notes
)
select
  batch.product_id,
  batch.id,
  batch.branch_id,
  'opening-balance',
  batch.quantity_received,
  batch.quantity_on_hand,
  batch.batch_code,
  batch.notes
from public.inventory_batches batch
where batch.source = 'opening-balance'
  and batch.quantity_received > 0
  and not exists (
    select 1
    from public.inventory_movements movement
    where movement.batch_id = batch.id
      and movement.movement_type = 'opening-balance'
  );

do $$
declare
  product_id_row record;
begin
  for product_id_row in
    select distinct product_id
    from public.inventory_batches
  loop
    perform private.recalculate_product_stock(product_id_row.product_id);
  end loop;
end
$$;

