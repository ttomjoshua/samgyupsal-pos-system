do $$
begin
  if exists (
    select 1
    from pg_constraint
    where conname = 'products_catalog_unique'
      and conrelid = 'public.products'::regclass
  ) then
    alter table public.products
      drop constraint products_catalog_unique;
  end if;

  if exists (
    select 1
    from (
      select
        branch,
        category_id,
        product_name,
        unit_label,
        count(*) as duplicate_count
      from public.products
      group by 1, 2, 3, 4
      having count(*) > 1
    ) duplicate_groups
  ) then
    create temporary table products_dedupe_map on commit drop as
    with ranked_products as (
      select
        p.id as product_id,
        min(p.id) over (
          partition by p.branch, p.category_id, p.product_name, p.unit_label
        ) as keep_product_id,
        p.branch,
        p.category_id,
        p.product_name,
        p.unit_label,
        p.default_price,
        p.legacy_price_text,
        p.is_active,
        p.created_at,
        p.updated_at
      from public.products p
    )
    select
      product_id,
      keep_product_id
    from ranked_products
    where exists (
      select 1
      from public.products duplicate_probe
      where duplicate_probe.branch = ranked_products.branch
        and duplicate_probe.category_id = ranked_products.category_id
        and duplicate_probe.product_name = ranked_products.product_name
        and duplicate_probe.unit_label = ranked_products.unit_label
      group by duplicate_probe.branch,
               duplicate_probe.category_id,
               duplicate_probe.product_name,
               duplicate_probe.unit_label
      having count(*) > 1
    );

    update public.products keep_product
    set
      default_price = product_rollup.default_price,
      legacy_price_text = product_rollup.legacy_price_text,
      is_active = product_rollup.is_active,
      created_at = product_rollup.created_at,
      updated_at = greatest(keep_product.updated_at, product_rollup.updated_at)
    from (
      select
        keep_product_id,
        max(default_price) as default_price,
        max(nullif(legacy_price_text, '')) as legacy_price_text,
        bool_or(coalesce(is_active, true)) as is_active,
        min(created_at) as created_at,
        max(updated_at) as updated_at
      from public.products product_member
      join products_dedupe_map dedupe_member
        on dedupe_member.product_id = product_member.id
      group by keep_product_id
    ) product_rollup
    where keep_product.id = product_rollup.keep_product_id;

    if to_regclass('public.inventory_items') is not null then
      create temporary table inventory_dedupe_rollup on commit drop as
      select
        coalesce(
          min(
            case
              when i.product_id = coalesce(product_dedupe.keep_product_id, i.product_id)
                then i.id
              else null
            end
          ),
          min(i.id)
        ) as keep_inventory_item_id,
        i.branch_id,
        coalesce(product_dedupe.keep_product_id, i.product_id) as keep_product_id,
        max(i.selling_price) as selling_price,
        sum(coalesce(i.stock_quantity, 0)) as stock_quantity,
        max(coalesce(i.reorder_level, 10)) as reorder_level,
        max(i.expiration_date) as expiration_date,
        max(nullif(i.legacy_stock_text, '')) as legacy_stock_text,
        max(nullif(i.notes, '')) as notes,
        bool_or(coalesce(i.is_active, true)) as is_active,
        min(i.created_at) as created_at,
        max(i.updated_at) as updated_at
      from public.inventory_items i
      left join products_dedupe_map product_dedupe
        on product_dedupe.product_id = i.product_id
      group by
        i.branch_id,
        coalesce(product_dedupe.keep_product_id, i.product_id);

      create temporary table inventory_dedupe_map on commit drop as
      select
        i.id as inventory_item_id,
        inventory_rollup.keep_inventory_item_id
      from public.inventory_items i
      left join products_dedupe_map product_dedupe
        on product_dedupe.product_id = i.product_id
      join inventory_dedupe_rollup inventory_rollup
        on inventory_rollup.branch_id = i.branch_id
       and inventory_rollup.keep_product_id = coalesce(product_dedupe.keep_product_id, i.product_id)
      where i.id <> inventory_rollup.keep_inventory_item_id;

      update public.inventory_items keep_inventory
      set
        product_id = inventory_rollup.keep_product_id,
        selling_price = inventory_rollup.selling_price,
        stock_quantity = inventory_rollup.stock_quantity,
        reorder_level = inventory_rollup.reorder_level,
        expiration_date = inventory_rollup.expiration_date,
        legacy_stock_text = inventory_rollup.legacy_stock_text,
        notes = inventory_rollup.notes,
        is_active = inventory_rollup.is_active,
        created_at = inventory_rollup.created_at,
        updated_at = greatest(keep_inventory.updated_at, inventory_rollup.updated_at)
      from inventory_dedupe_rollup inventory_rollup
      where keep_inventory.id = inventory_rollup.keep_inventory_item_id;

      if to_regclass('public.sale_items') is not null then
        update public.sale_items sale_item
        set inventory_item_id = inventory_dedupe.keep_inventory_item_id
        from inventory_dedupe_map inventory_dedupe
        where sale_item.inventory_item_id = inventory_dedupe.inventory_item_id;
      end if;

      delete from public.inventory_items inventory_item
      using inventory_dedupe_map inventory_dedupe
      where inventory_item.id = inventory_dedupe.inventory_item_id;
    end if;

    if to_regclass('public.sale_items') is not null then
      update public.sale_items sale_item
      set product_id = product_dedupe.keep_product_id
      from products_dedupe_map product_dedupe
      where sale_item.product_id = product_dedupe.product_id
        and sale_item.product_id <> product_dedupe.keep_product_id;
    end if;

    delete from public.products duplicate_product
    using products_dedupe_map product_dedupe
    where duplicate_product.id = product_dedupe.product_id
      and duplicate_product.id <> product_dedupe.keep_product_id;
  end if;

  if not exists (
    select 1
    from pg_constraint
    where conname = 'products_catalog_unique'
      and conrelid = 'public.products'::regclass
  ) then
    if exists (
      select 1
      from pg_class relation
      join pg_namespace namespace
        on namespace.oid = relation.relnamespace
      where relation.relname = 'products_catalog_unique'
        and namespace.nspname = 'public'
    ) then
      drop index if exists public.products_catalog_unique;
    end if;

    alter table public.products
      add constraint products_catalog_unique
      unique (branch, category_id, product_name, unit_label);
  end if;
end
$$;

