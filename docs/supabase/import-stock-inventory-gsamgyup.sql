  (451, 2, 'Dollar', 'Uncategorized', 'SICHUAN HOT', '', 0.00, 1, null, '2026-04-14 11:43:44')
),
collapsed_import_rows as (
  select
    min(id) as id,
    min(source_branch_id) as source_branch_id,
    branch,
    category,
    product_name,
    net_weight,
    max(price) as price,
    sum(stock_quantity)::integer as stock_quantity,
    min(expiration_date::date) as expiration_date,
    min(source_created_at) as source_created_at
  from import_rows
  group by
    branch,
    category,
    product_name,
    net_weight
),
upserted_products as (
  insert into public.products (
    id,
    product_name,
    branch,
    category,
    net_weight,
    price,
    stock_quantity,
    expiration_date
  )
  overriding system value
  select
    id,
    product_name,
    branch,
    category,
    net_weight,
    price,
    stock_quantity,
    expiration_date::date
  from collapsed_import_rows
  on conflict (id) do update
  set
    product_name = excluded.product_name,
    branch = excluded.branch,
    category = excluded.category,
    net_weight = excluded.net_weight,
    price = excluded.price,
    stock_quantity = excluded.stock_quantity,
    expiration_date = excluded.expiration_date
  returning id, branch
)
select
  count(*) as upserted_products_count,
  count(*) filter (where branch = 'Sta. Lucia') as sta_lucia_products_count,
  count(*) filter (where branch = 'Dollar') as dollar_products_count
from upserted_products;

select setval(
  pg_get_serial_sequence('public.products', 'id'),
  greatest((select coalesce(max(id), 1) from public.products), 1),
  true
);

